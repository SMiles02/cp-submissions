fun solve() {
    var (a, b) = readInts()
    if (a==b)
        println(0)
    else if (a>b)
    {
        if ((a-b)%2==1)
            println(2)
        else
            println(1)
    }
    else
    {
        if ((b-a)%2==0)
            println(2)
        else
            println(1)
    }
}

fun main(args: Array<String>) {
    val T = readInt()
    for (i in 1..T) solve()
}

